# Funções matematicas.
import math
print("PI: ")
print(math.pi)
print("Raiz Quadrada: 81")
print(math.sqrt(81))
print("Valor absoluto de -10")
print(math.fabs(-10))
print("Fatorial de 10")
print(math.factorial(10))
print("log de 10")
print(math.log10(10))
print("2 elevado a 10")
print(math.pow(2, 10))
print("Arredondar 25.9 para cima")
print(math.ceil(25.9))
print("Arredondar 25.9 para baixo")
print(math.floor(25.9))
print("Truncar 25.9")
print(math.trunc(25.9))









    

      
